#include <stdio.h>
#include<stdlib.h>

void Swap(int * p , int * q)
{
    int temp = 0;
    temp = *p;
    *p = *q;
    *q = *p;
}
int main()
{
    int iValue1 = 11 , iValue2 = 21 ;

    printf("Before Swap : %d %d\n",iValue1,iValue2);

    Swap(&iValue1,&iValue2);

    printf("After Swap : %d %d\n",iValue1,iValue2);

    return 0 ;
}